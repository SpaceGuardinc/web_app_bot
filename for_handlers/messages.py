from string import punctuation

from aiogram.types import Message

restricted_words = {'—Ö—É–π', '—Å—É–∫–∞', '–¥–∞—É–Ω', '–∏–º–±–∏—Ü–∏–ª', '–¥—É—Ä–∞–∫', '–µ–±–∞–ª–æ', '–ø–∏–∑–¥–∞', 'fuck', 'trash', 'shit', '–ø–∏–∑–¥–µ—Ü',
                    '–ø–∏–∑–¥–∞—Ä–∞—Å', '—É–µ–±–æ–∫', '—Ö—É–π–ª–æ', '–ø–∏–∑–¥–∞–ª–∏–∑', '—Ö—É–µ—Å–æ—Å', '–∫–æ–Ω—á–µ–Ω–Ω—ã–π', 'retard', '–µ–±–∞–ª',
                    '—Ö—É–π–Ω—è', '–ª–æ—Ö', '–≥–æ–≤–Ω—é–∫', '–∂–æ–ø–∞', '–ª–æ—à–∞—Ä–∞', '–Ω–∞—Ö—É–π', '–ø–∏–∑–¥—É'}


# # –∑–∞—â–∏—Ç–∞ –æ—Ç —Å–∏–º–≤–æ–ª–æ–≤ –º–µ–∂–¥—É –ø–ª–æ—Ö–∏–º–∏ —Å–ª–æ–≤–∞–º–∏
def clean_text(text: str):
    return text.translate(str.maketrans('', '', punctuation))


# # –∑–∞—â–∏—Ç–∞ –æ—Ç –ø–ª–æ—Ö–∏—Ö —Å–ª–æ–≤, —Ç–∞–∫–∂–µ –±–∞–Ω —ç—Ç–æ–≥–æ —é–∑–µ—Ä–∞ (–≤ –∫–æ–º–º–µ–Ω—Ç–µ)

async def get_shop_message(message: Message) -> str:
    if restricted_words.intersection(clean_text(message.text.lower()).split()):
        return (
            "–ù–µ —Ä—É–≥–∞–π—Ç–µ—Å—å –ø–æ–∂–∞–ª—É–π—Å—Ç–∞ üòä"
        )
    else:
        return (
            "–í–µ—Å—å —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª –¥–æ—Å—Ç—É–ø–µ–Ω –≤ –Ω–∞—à–µ–º –∏–Ω—Ç–µ—Ä–Ω–µ—Ç-–º–∞–≥–∞–∑–∏–Ω–µ.\n"
            '–ù–∞–∂–º–∏—Ç–µ "–û—Ç–∫—Ä—ã—Ç—å –º–∞–≥–∞–∑–∏–Ω", —á—Ç–æ–±—ã –ø—Ä–∏—Å—Ç—É–ø–∏—Ç—å –∫ –ø–æ–∫—É–ø–∫–∞–º'
        )
